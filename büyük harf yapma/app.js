function bYap(){
let cumle = document.getElementById("cumle").value;
let buyukCumle = cumle.toUpperCase();
let yaz = document.getElementById("yaz");
yaz.textContent = buyukCumle;
}

function kYap(){
let cumle = document.getElementById("cumle").value;
let kucukCumle = cumle.toLowerCase();
let yaz = document.getElementById("yaz");
yaz.textContent = kucukCumle;
}